package com.mabrikoli.enums;

/**
 * Types of verification documents that artisans attach to their applications.
 */
public enum DocumentType {

    /** Government-issued identity card or passport */
    ID_CARD,

    /** Professional diploma or degree */
    DIPLOMA,

    /** Trade or vocational certificate */
    CERTIFICATE,

    /** Utility bill or official document confirming address */
    PROOF_OF_ADDRESS,

    /** Any other supporting document */
    OTHER
}
